# Python 算数运算符
# + 加法: 2 + 1 输出结果 3
print(2 + 1)
# - 减法: 1 - 2 输出结果 -1
print(1 - 2)
# * 乘法: 1 * 2 输出结果 2
print(1 * 2)
# / 除法: 1 / 2 输出结果 0.5
print(1 / 2)
print(type(1 / 2))
# % 取模: 9 % 4 输出结果 1
print(9 % 4)
# ** 幂: 2 ** 3 输出结果 8
print(2 ** 3)
# // 取整除: 9 // 4 输出结果 2
print(9 // 4)
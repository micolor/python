# 字符串的定义和打印

# 单引号字符串
str1 = '两点水'  
# 双引号字符串
str2 = "两点水"  
# 三引号字符串，可以包含换行
str3 = '''两点水''' 
print(str1) 
print(str2)
print(str3)

# 包含单引号的字符串
str4 = "'hello world'" 
# 包含双引号的字符串
str5 = '"hello world"'
# 使用三引号定义多行字符串
str6 = '''hello 
world'''  
print(str4) 
print(str5)  
print(str6) 
# 第一个 Python 程序
print('hello python')  # 输出字符串 'hello python'

# 打印多个值
print('Hello', 'python')  # 输出 'Hello python'

# 使用格式化字符串
name = 'Python'
print(f'Hello, {name}!')  # 输出 'Hello, Python!'

# 使用字符串连接
print('Hello' + ' ' + 'Python')  # 输出 'Hello Python'

# 打印数字
print(123)  # 输出 123
print(3.14)  # 输出 3.14

# 打印表达式结果
print(1 + 2)  # 输出 3
print(2 * 3)  # 输出 6